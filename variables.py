# Integers (int): These represent positive or negative whole numbers without fractions or decimals. There’s no limit
# to how long an integer value can be

# Floating-Point Numbers (float): These are real numbers with a decimal point. Optionally, they can be expressed in
# scientific notation using the character ‘e’ or ‘E’ followed by an integer

# Strings (str): Used to store text data. Strings are enclosed in single or double quotes.

# Booleans (bool): These have only two possible values: True or False. They are often used for logical operations and
# control flow.

